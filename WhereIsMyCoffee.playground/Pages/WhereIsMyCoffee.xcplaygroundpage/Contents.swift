 ## Coffee is not simple
 
 Every morning I have a cup of coffee to start my day well, besides giving me a boost of energy, it is also very pleasent to taste. However, sometimes we donâ€™t even stop to ask ourselves where it came from and what our coffee went through.
 
 First of all it comes from a fruit that is made by a plant called *Coffea*, it is a small tree native to tropical and southern Africa and tropical Asia. There are over 120 differente species of *Coffea* but the most popular one is the *Coffea Arabica*.
 
 After being harvested, the coffee fruits goes through many different processes (many of which are optional) until it can finally be roasted. They usually  include drying and peeling.

 The roasting process is very important and complex because it includes many variables that will impact how the coffee tastes in the end. The rosting profiles can be divided into 4 main types: **light roast**, **medium roast**, **medium dark roast** and **dark roast**. Although there is not a better roasting profile, it all dependes on what you are looking for in your coffee. The longer a coffee is roasted, the more the heat influences the natural flavour of the coffee beans. In general if you like more **pronounced bitterness** and **less acidity** in your cup, you should go with a **darker roast**, and if you like a **lighter body** and **higher acidity**, you should go with a **lighter roast**.
 
 After all that, the only things that are left is to grind the coffee to the desired method, prepare it and enjoy!

